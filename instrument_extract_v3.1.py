def clean_str(value) -> str:
    if value is None:
        return ""
    return str(value).strip()


def normalize_text(text: str) -> str:
    text = str(text or "")
    text = text.replace("\u00a0", " ")
    text = re.sub(r"[ \t]+", " ", text)
    text = re.sub(r"\n{3,}", "\n\n", text)
    return text.strip()


def safe_float(value, default: float = 0.0) -> float:
    try:
        return float(value)
    except Exception:
        return default


def safe_int(value, default: int = 0) -> int:
    try:
        return int(float(value))
    except Exception:
        return default


def safe_int01(value) -> int:
    if isinstance(value, bool):
        return 1 if value else 0
    if isinstance(value, int):
        return 1 if value == 1 else 0
    if isinstance(value, float):
        return 1 if int(value) == 1 else 0

    s = clean_str(value).lower()
    if s in {"1", "true", "yes", "present"}:
        return 1
    return 0


def normalize_bool(value) -> bool:
    if isinstance(value, bool):
        return value
    return clean_str(value).lower() in {"true", "yes", "1"}


def load_csv(path: Path) -> List[Dict[str, Any]]:
    if not path.exists():
        raise FileNotFoundError(f"File not found: {path}")
    with open(path, "r", encoding="utf-8-sig", newline="") as f:
        return list(csv.DictReader(f))


def load_optional_csv(path: Path) -> List[Dict[str, Any]]:
    if not path.exists():
        return []
    with open(path, "r", encoding="utf-8-sig", newline="") as f:
        return list(csv.DictReader(f))


def save_csv(path: Path, rows: List[Dict[str, Any]], fieldnames: List[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(rows)


def remove_policy_rows(rows: List[Dict[str, Any]], policy_id: str) -> List[Dict[str, Any]]:
    return [row for row in rows if clean_str(row.get("policy_id")) != policy_id]


def parse_json_object(text: str) -> Dict[str, Any]:
    text = text.strip()
    match = re.search(r"\{.*\}", text, re.S)
    if not match:
        raise ValueError("No JSON object found in model output")
    return json.loads(match.group(0))


def simpson_balance(type_counts: Dict[str, int]) -> float:
    counts = [v for v in type_counts.values() if v > 0]
    total = sum(counts)

    if total <= 1:
        return 0.0

    numerator = sum(n * (n - 1) for n in counts)
    denominator = total * (total - 1)
    return round(1 - numerator / denominator, 4)


