def retrieve_seed_chunks(
    policy_id: str,
    retrieval_query_embeddings: List[List[float]],
) -> List[Dict[str, Any]]:
    if not retrieval_query_embeddings:
        return []

    seeds = {}

    for query_emb in retrieval_query_embeddings:
        try:
            res = collection.query(
                query_embeddings=[query_emb],
                where={"policy_id": policy_id},
                n_results=N_RESULTS_PER_QUERY,
                include=["documents", "metadatas", "distances"],
            )

            docs = res.get("documents", [[]])[0]
            metas = res.get("metadatas", [[]])[0]
            distances = res.get("distances", [[]])[0]

            for rank, (doc, meta, dist) in enumerate(zip(docs, metas, distances)):
                cid = clean_str(meta.get("chunk_id"))
                if not cid:
                    continue

                rank_score = 1.0 / (rank + 1)

                if cid not in seeds:
                    seeds[cid] = {
                        "chunk_id": cid,
                        "text": normalize_text(doc),
                        "section_title": clean_str(meta.get("section_title")),
                        "retrieval_score": 0.0,
                        "retrieval_hits": 0,
                        "best_distance": dist,
                    }

                seeds[cid]["retrieval_score"] += rank_score
                seeds[cid]["retrieval_hits"] += 1

                try:
                    seeds[cid]["best_distance"] = min(seeds[cid]["best_distance"], dist)
                except Exception:
                    pass

        except Exception as e:
            print(f"[Seed retrieval warning] {policy_id}: {e}")
            return []

    seed_chunks = list(seeds.values())
    seed_chunks.sort(
        key=lambda ch: (
            float(ch.get("retrieval_score", 0.0)),
            int(ch.get("retrieval_hits", 0)),
        ),
        reverse=True,
    )

    return seed_chunks[:MAX_SEED_CHUNKS_PER_POLICY]


def build_context_windows_from_seeds(
    all_chunks: List[Dict[str, Any]],
    seed_chunks: List[Dict[str, Any]],
) -> List[List[Dict[str, Any]]]:
    if not all_chunks:
        return []

    all_chunks = sort_chunks_by_chunk_id(all_chunks)

    chunk_by_id = {}
    ordered_ids = []

    for ch in all_chunks:
        cid = clean_str(ch.get("chunk_id"))
        if not cid:
            continue

        chunk_by_id[cid] = {
            "chunk_id": cid,
            "text": clean_str(ch.get("text")),
            "section_title": clean_str(ch.get("section_title")),
        }
        ordered_ids.append(cid)

    id_to_pos = {cid: i for i, cid in enumerate(ordered_ids)}
    raw_windows = []

    for seed in seed_chunks:
        seed_id = clean_str(seed.get("chunk_id"))
        if seed_id not in id_to_pos:
            continue

        seed_pos = id_to_pos[seed_id]
        seed_section = clean_str(chunk_by_id[seed_id].get("section_title"))

        start_pos = max(0, seed_pos - CONTEXT_EXPAND_BEFORE)
        end_pos = min(len(ordered_ids) - 1, seed_pos + CONTEXT_EXPAND_AFTER)

        window_ids = []

        for pos in range(start_pos, end_pos + 1):
            cid = ordered_ids[pos]
            ch = chunk_by_id[cid]
            ch_section = clean_str(ch.get("section_title"))

            if seed_section and ch_section and ch_section != seed_section:
                continue

            window_ids.append(cid)

        if not window_ids:
            window_ids = [seed_id]

        raw_windows.append({
            "seed_id": seed_id,
            "seed_score": float(seed.get("retrieval_score", 0.0)),
            "section_title": seed_section,
            "chunk_ids": set(window_ids),
        })

    merged = []

    for win in raw_windows:
        placed = False

        for existing in merged:
            same_section = (
                clean_str(existing.get("section_title"))
                and clean_str(existing.get("section_title")) == clean_str(win.get("section_title"))
            )

            overlap = bool(existing["chunk_ids"].intersection(win["chunk_ids"]))

            existing_positions = [id_to_pos[cid] for cid in existing["chunk_ids"] if cid in id_to_pos]
            new_positions = [id_to_pos[cid] for cid in win["chunk_ids"] if cid in id_to_pos]

            adjacent = False
            if existing_positions and new_positions:
                adjacent = min(abs(a - b) for a in existing_positions for b in new_positions) <= 1

            if overlap or (same_section and adjacent):
                existing["chunk_ids"].update(win["chunk_ids"])
                existing["seed_score"] = max(existing["seed_score"], win["seed_score"])
                placed = True
                break

        if not placed:
            merged.append(win)

    context_windows = []

    for win in merged:
        ids_sorted = sorted(
            win["chunk_ids"],
            key=lambda cid: id_to_pos.get(cid, 999999),
        )

        current = []
        current_chars = 0

        for cid in ids_sorted:
            ch = chunk_by_id[cid]
            ch_len = len(clean_str(ch.get("text")))

            if current and current_chars + ch_len > MAX_CONTEXT_CHARS:
                context_windows.append(current)
                current = []
                current_chars = 0

            current.append(ch)
            current_chars += ch_len

        if current:
            context_windows.append(current)

    def window_score(window):
        ids = {clean_str(ch.get("chunk_id")) for ch in window}
        score = 0.0

        for seed in seed_chunks:
            if clean_str(seed.get("chunk_id")) in ids:
                score += float(seed.get("retrieval_score", 0.0))

        return score

    context_windows.sort(
        key=lambda w: (
            window_score(w),
            -get_chunk_order_value(w[0]) if w else -999999,
        ),
        reverse=True,
    )

    context_windows = context_windows[:MAX_CONTEXT_WINDOWS_PER_POLICY]
    context_windows.sort(key=lambda w: get_chunk_order_value(w[0]) if w else 999999)

    return context_windows


def build_context_window_batches(context_windows: List[List[Dict[str, Any]]]) -> List[str]:
    batches = []

    for batch_i, window in enumerate(context_windows, start=1):
        parts = []

        for j, ch in enumerate(window, start=1):
            chunk_id = clean_str(ch.get("chunk_id"))
            section = clean_str(ch.get("section_title")) or "UNKNOWN_SECTION"
            text = clean_str(ch.get("text"))

            block = (
                f"[ContextBatch {batch_i} | Chunk {j} | chunk_id={chunk_id} | "
                f"Section: {section}]\n{text}\n"
            )
            parts.append(block)

        batches.append("\n\n".join(parts).strip())

    return batches


def build_chunk_batches(
    chunks: List[Dict[str, Any]],
    max_chars: int = MAX_CHARS_PER_BATCH,
) -> List[str]:
    batches = []
    current_parts = []
    current_len = 0

    for i, ch in enumerate(chunks, start=1):
        section = clean_str(ch.get("section_title")) or "UNKNOWN_SECTION"
        chunk_id = clean_str(ch.get("chunk_id")) or str(i)
        text = clean_str(ch.get("text"))

        if not text:
            continue

        block = f"[FullBatch | Chunk {i} | chunk_id={chunk_id} | Section: {section}]\n{text}\n"

        if current_parts and current_len + len(block) > max_chars:
            batches.append("\n\n".join(current_parts).strip())
            current_parts = []
            current_len = 0

        current_parts.append(block)
        current_len += len(block)

    if current_parts:
        batches.append("\n\n".join(current_parts).strip())

    if MAX_BATCHES_PER_POLICY is not None:
        batches = batches[:MAX_BATCHES_PER_POLICY]

    return batches


def retrieve_context_batches_for_policy(
    policy_id: str,
    all_chunks: List[Dict[str, Any]],
    retrieval_query_embeddings: List[List[float]],
) -> Tuple[List[str], int, int]:
    seed_chunks = retrieve_seed_chunks(
        policy_id=policy_id,
        retrieval_query_embeddings=retrieval_query_embeddings,
    )

    if not seed_chunks:
        print("  - No seed chunks found; fallback to full-text batches")
        batches = build_chunk_batches(all_chunks)
        return batches, 0, len(all_chunks)

    context_windows = build_context_windows_from_seeds(
        all_chunks=all_chunks,
        seed_chunks=seed_chunks,
    )

    if not context_windows:
        print("  - No context windows built; fallback to full-text batches")
        batches = build_chunk_batches(all_chunks)
        return batches, len(seed_chunks), len(all_chunks)

    batches = build_context_window_batches(context_windows)

    unique_context_chunk_ids = set()
    for window in context_windows:
        for ch in window:
            cid = clean_str(ch.get("chunk_id"))
            if cid:
                unique_context_chunk_ids.add(cid)

    if MAX_BATCHES_PER_POLICY is not None:
        batches = batches[:MAX_BATCHES_PER_POLICY]

    return batches, len(seed_chunks), len(unique_context_chunk_ids)


