def get_chunk_order_value(ch: Dict[str, Any]) -> int:
    cid = clean_str(ch.get("chunk_id"))
    try:
        return int(float(cid))
    except Exception:
        return 999999


def sort_chunks_by_chunk_id(chunks: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    return sorted(chunks, key=get_chunk_order_value)


def get_policy_chunks(policy_id: str) -> List[Dict[str, Any]]:
    data = collection.get(
        where={"policy_id": policy_id},
        include=["documents", "metadatas"],
    )

    docs = data.get("documents", [])
    metas = data.get("metadatas", [])

    chunks = []
    for doc, meta in zip(docs, metas):
        chunks.append({
            "text": normalize_text(doc),
            "section_title": clean_str(meta.get("section_title")),
            "chunk_id": meta.get("chunk_id"),
        })

    return sort_chunks_by_chunk_id(chunks)


