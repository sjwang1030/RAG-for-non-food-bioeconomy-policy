def merge_policy_records(records: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    grouped = {}

    for rec in records:
        harmonized = clean_str(rec.get("instrument_subtype_harmonized"))
        domain = clean_str(rec.get("linked_domain"))
        if not harmonized or not domain:
            continue

        key = (harmonized.lower(), domain.lower())

        if key not in grouped:
            grouped[key] = {
                "records": [],
                "examples": [],
                "batch_ids": set(),
                "raw_subtypes": set(),
            }

        grouped[key]["records"].append(rec)

        text = clean_str(rec.get("measure_text"))
        if text and text not in grouped[key]["examples"]:
            grouped[key]["examples"].append(text)

        batch_id = clean_str(rec.get("batch_id"))
        if batch_id:
            grouped[key]["batch_ids"].add(batch_id)

        raw_subtype = clean_str(rec.get("instrument_subtype"))
        if raw_subtype:
            grouped[key]["raw_subtypes"].add(raw_subtype)

    merged = []

    for item in grouped.values():
        group_records = item["records"]

        def representative_score(r):
            return (
                safe_float(r.get("confidence"), 0.0),
                int(r.get("calibration_score", 0)),
                -len(clean_str(r.get("measure_text"))),
            )

        best = sorted(group_records, key=representative_score, reverse=True)[0]

        representative_calibration = {}
        for field in CALIBRATION_FIELDS:
            representative_calibration[field] = int(best.get(field, 0))

        representative_calibration_score = sum(representative_calibration.values())
        calibrated_yes_no = 1 if representative_calibration_score >= CALIBRATION_THRESHOLD else 0

        avg_confidence = sum(safe_float(r.get("confidence"), 0.0) for r in group_records) / len(group_records)
        max_confidence = max(safe_float(r.get("confidence"), 0.0) for r in group_records)

        evidence_examples = " || ".join(item["examples"][:3])
        evidence_batch_ids = ";".join(
            sorted(item["batch_ids"], key=lambda x: int(x) if x.isdigit() else 999999)
        )
        raw_subtypes_collapsed = " || ".join(sorted(item["raw_subtypes"]))

        merged.append({
            "instrument_type": clean_str(best.get("instrument_type")),
            "instrument_group": clean_str(best.get("instrument_group")),
            "instrument_subtype": clean_str(best.get("instrument_subtype")),
            "instrument_subtype_harmonized": clean_str(best.get("instrument_subtype_harmonized")),
            "raw_subtypes_collapsed": raw_subtypes_collapsed,
            "linked_domain": clean_str(best.get("linked_domain")),
            "evidence_mention_count": len(group_records),
            "evidence_batch_ids": evidence_batch_ids,
            "representative_measure_text": clean_str(best.get("measure_text")),
            "representative_justification": clean_str(best.get("justification")),
            "evidence_examples": evidence_examples,
            **representative_calibration,
            "calibration_score": representative_calibration_score,
            "calibrated_yes_no": calibrated_yes_no,
            "confidence": round(max_confidence, 3),
            "avg_confidence_internal": round(avg_confidence, 3),
        })

    merged.sort(
        key=lambda r: (
            clean_str(r.get("instrument_type")),
            clean_str(r.get("instrument_subtype_harmonized")),
            clean_str(r.get("linked_domain")),
        )
    )

    return merged


