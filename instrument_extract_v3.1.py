def validate_records(
    raw_records: List[Dict[str, Any]],
    batch_id: int,
) -> List[Dict[str, Any]]:
    cleaned = []

    for rec in raw_records:
        decision = clean_str(rec.get("final_decision")).lower()
        if decision != "keep":
            continue

        measure_text = normalize_text(clean_str(rec.get("measure_text")))
        if not measure_text:
            continue

        evidence_role = clean_str(rec.get("evidence_role")).lower()
        jurisdiction_attr = clean_str(rec.get("jurisdiction_attribution")).lower()
        relevance = clean_str(rec.get("nonfood_bioeconomy_relevance")).lower()
        linked_domain = normalize_domain(rec.get("linked_domain"))

        if evidence_role != "proposed_instrument":
            continue
        if jurisdiction_attr != "focal_jurisdiction":
            continue
        if relevance != "direct":
            continue
        if linked_domain not in ALLOWED_DOMAINS:
            continue
        if not normalize_bool(rec.get("is_concrete_instrument")):
            continue

        subtype = clean_str(rec.get("instrument_subtype"))
        if subtype not in ALLOWED_SUBTYPES:
            continue

        confidence = safe_float(rec.get("confidence"), 0.0)
        if confidence < MIN_CONFIDENCE_KEEP:
            continue

        true_type = SUBTYPE_TO_TYPE[subtype]
        true_group = SUBTYPE_TO_GROUP[subtype]
        harmonized_subtype = harmonize_subtype(subtype)

        calibration_values = {}
        for field in CALIBRATION_FIELDS:
            calibration_values[field] = safe_int01(rec.get(field))

        calibration_score = sum(calibration_values.values())

        cleaned.append({
            "batch_id": batch_id,
            "measure_text": measure_text,
            "linked_domain": linked_domain,
            "instrument_type": true_type,
            "instrument_group": true_group,
            "instrument_subtype": subtype,
            "instrument_subtype_harmonized": harmonized_subtype,
            **calibration_values,
            "calibration_score": calibration_score,
            "justification": normalize_text(clean_str(rec.get("justification"))),
            "confidence": round(confidence, 3),
        })

    return cleaned


