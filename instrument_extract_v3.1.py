def get_dominant_instrument_type(row: Dict[str, Any]) -> Tuple[str, float]:
    total = safe_float(row.get("instrument_record_count"), 0.0)

    if total <= 0:
        return "none", 0.0

    type_counts = {
        instrument_type: safe_float(row.get(instrument_type), 0.0)
        for instrument_type in INSTRUMENT_TYPES
    }

    dominant_type = max(type_counts, key=type_counts.get)
    dominant_count = type_counts[dominant_type]
    dominant_share = dominant_count / total if total else 0.0

    return dominant_type, round(dominant_share, 4)


def classify_policy_mix_profile(row: Dict[str, Any]) -> str:
    total = safe_int(row.get("instrument_record_count"), 0)
    distinct_types = safe_int(row.get("distinct_instrument_types"), 0)
    simpson = safe_float(row.get("Simpson_balance"), 0.0)
    dominant_type, dominant_share = get_dominant_instrument_type(row)

    if total == 0:
        return "no_instrument_detected"

    if distinct_types <= 1:
        return "single_type_policy"

    if dominant_share >= 0.6:
        return f"{dominant_type.lower()}_dominant_policy"

    if distinct_types >= 3 and simpson >= 0.55:
        return "balanced_policy_mix"

    return "mixed_policy"


